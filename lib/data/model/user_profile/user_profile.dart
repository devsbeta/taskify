class Assigned {
  int? projects;
  int? tasks;

  Assigned({this.projects, this.tasks});

  Assigned.fromJson(Map<String, dynamic> json) {
    projects = json['projects'];
    tasks = json['tasks'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['projects'] = projects;
    data['tasks'] = tasks;
    return data;
  }
}
